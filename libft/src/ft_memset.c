#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	unsigned char	*p;
	size_t			i;

	p = (unsigned char *)b;
	i = 0;
	while (n-- > 0)
	{
		p[i] = c;
		i++;
	}
	return (b);
}
