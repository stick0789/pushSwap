#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*rtn;

	rtn = malloc(size * nmemb);
	if (!rtn)
		return (NULL);
	ft_bzero(rtn, size * nmemb);
	return (rtn);
}
